import UIKit

extension UIColor {
    
    open class var hboBlue: UIColor {
        return UIColor(displayP3Red: 86/255, green: 178/255, blue: 220/255, alpha: 1)
    }
    
    open class var darkBackground: UIColor {
        return UIColor(displayP3Red: 22/255, green: 22/255, blue: 22/255, alpha: 1)
    }
    
}
